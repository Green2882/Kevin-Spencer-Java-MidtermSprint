
public abstract class Person {

    // Instance variables
    private String ID;
    private String name;
    private String age;
    private String phoneNumber;

    // Constrcutor
    Person(String ID, String name, String age, String phoneNumber) {

        this.ID = ID;
        this.name = name;
        this.age = age;
        this.phoneNumber = phoneNumber;
    }

}
